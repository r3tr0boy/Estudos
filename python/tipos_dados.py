# Tipo inteiro
idade = 18
print(type(idade))
# Tipo string
nome = "Pedro"
print(type(nome))
# Tipo float
pi = 3.141
print(type(pi))
# Tipo booleano
online = True
print(type(online))
# Listas
lista = [1,2,3,4,5]
print(type(lista))
# Tuplas
tupla = (5,4,3,2,1)
print(type(tupla))
# Dicionarios
dicionario = {"nome": "joao", "idade": 18}
print(type(dicionario))
# Tipos complexos
a = 5+2j
print(type(a))